package day2;

public class Var1 {
	
	public static void main(String[] args) {
		int age = 45;
		
		System.out.println("Your Age Is: "+age);
		byte n = 120;
		System.out.println("Byte Number n Is: "+n);
		byte num = (byte)1200;
		System.out.println("Byte Number Is: "+num);
	
		long longNumber = 45000;
		System.out.println("Long Number = "+longNumber);
		long longNumberNew = 2147483648L;
//		The literal 2147483648 of type int is out of range
		System.out.println("Long Number New = "+longNumberNew);
		
		float pi = 3.14F;
		System.out.println("Value of PI is: "+pi);
		
		double g = 9.81;
		
		System.out.println("Value of Gravity = "+g);
		
		
	
	}

}
