package day11.inheritance.parent;

public class Animal {

	public Animal() {
		System.out.println("Animal is created");
	}

	public void walk() {
		System.out.println("Animal Is Walking");
	}

	public void eat() {
		System.out.println("Animal Is Eating");

	}

	public void sleep() {
		System.out.println("Animal Is Sleeping");

	}

}
